    private void botaoSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoSalvarActionPerformed
        ms.nome = TextNome.getText();
        TextNome.setText("");

        if (ProgBox.isSelected()) {
            lista.add("Prog Comercial");
            ProgBox.setSelected(false);
        }
        if (PsyProgBox.isSelected()) {
            lista.add("Psy Progressive");
            PsyProgBox.setSelected(false);
        }
        if (GrooveBox.isSelected()) {
            lista.add("Full On Groove");
            GrooveBox.setSelected(false);
        }
        if (MorningBox.isSelected()) {
            lista.add("Full On Morning");
            MorningBox.setSelected(false);
        }
        if (GoaBox.isSelected()) {
            lista.add("Goa");
            GoaBox.setSelected(false);
        }
        if (NightBox.isSelected()) {
            lista.add("Full On Night");
            NightBox.setSelected(false);
        }
        if (TwilightBox.isSelected()) {
            lista.add("Twilight");
            TwilightBox.setSelected(false);
        }
        if (ForestBox.isSelected()) {
            lista.add("Forest");
            ForestBox.setSelected(false);
        }
        if (DarkBox.isSelected()) {
            lista.add("Dark");
            DarkBox.setSelected(false);
        }
        if (HiBox.isSelected()) {
            lista.add("Hi-Tech");
            HiBox.setSelected(false);
        }
        
        try {
            aq.salvarArquivo(ms.nome,lista);
        } catch (IOException ex) {
            Logger.getLogger(Menu.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        String novoCadastro = ms.nome;
        
        listaCadastros.setModel(dlm);
        dlm.addElement(novoCadastro);
        
        JOptionPane.showMessageDialog(null, "Adicionado com sucesso!");
    }//GEN-LAST:event_botaoSalvarActionPerformed

