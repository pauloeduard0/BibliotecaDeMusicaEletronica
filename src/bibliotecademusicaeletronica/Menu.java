    private void botaoBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoBuscarActionPerformed
        
        pesquisar = (String) jComboBox1.getSelectedItem();
        try {
            aq.buscarArquivo(pesquisar);
        } catch (IOException ex) {
            Logger.getLogger(Menu.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_botaoBuscarActionPerformed

    private void botaoEncontrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoEncontrarActionPerformed
        ms.nomeErrado = busca2text.getText();
        ms.nomeNovo = novotext.getText();
        try {
            aq.encontrarArquivo(ms.nomeErrado, ms.nomeNovo);
        } catch (IOException ex) {
            Logger.getLogger(Menu.class.getName()).log(Level.SEVERE, null, ex);
        }
        busca2text.setText("");
        novotext.setText("");
        JOptionPane.showMessageDialog(null, "Alterado com sucesso!");
    }//GEN-LAST:event_botaoEncontrarActionPerformed

